package application.rdg;

public class ExtentStatistic {
    private Integer player_id;
    private Integer number_of_kills;
    private Integer week;

    public Integer getPlayer_id() {
        return player_id;
    }

    public void setPlayer_id(Integer player_id) {
        this.player_id = player_id;
    }

    public Integer getNumber_of_kills() {
        return number_of_kills;
    }

    public void setNumber_of_kills(Integer number_of_kills) {
        this.number_of_kills = number_of_kills;
    }

    public Integer getWeek() {
        return week;
    }

    public void setWeek(Integer week) {
        this.week = week;
    }
}
